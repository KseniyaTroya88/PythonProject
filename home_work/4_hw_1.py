# a. Создайте конструктор класса Car. Создайте атрибуты класса Car — color (цвет), type (тип), year (год).
# b. Напишите пять методов.
# - Первый — запуск автомобиля, при его вызове выводится сообщение «Автомобиль заведен».
# - Второй — отключение автомобиля — выводит сообщение «Автомобиль заглушен».
# - Третий — присвоение автомобилю года выпуска.
# - Четвертый метод — присвоение автомобилю типа.
# - Пятый — присвоение автомобилю цвета.

class Car:
    def __init__(self, color=None, type=None, year=None):
        """Конструктор класса Car"""
        self.color = color
        self.type = type
        self.year = year

    def start_engine(self):
        """Запуск двигателя автомобиля"""
        print("Автомобиль заведен")

    def stop_engine(self):
        """Остановка двигателя автомобиля"""
        print("Автомобиль заглушен")

    def set_year(self, year):
        """Установка года выпуска"""
        self.year = year
        print(f"Год выпуска установлен: {year}")

    def set_type(self, type):
        """Установка типа автомобиля"""
        self.type = type
        print(f"Тип автомобиля установлен: {type}")

    def set_color(self, color):
        """Установка цвета автомобиля"""
        self.color = color
        print(f"Цвет автомобиля установлен: {color}")


# Пример использования
if __name__ == "__main__":
    # Создаем экземпляр автомобиля
    my_car = Car(color="Красный", type="Седан", year=2020)

    # Используем методы
    my_car.start_engine()
    my_car.set_year(2012)
    my_car.set_type("Универсал")
    my_car.set_color("Синий")
    my_car.stop_engine()