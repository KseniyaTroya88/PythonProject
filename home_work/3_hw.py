# 5. Функция на вход получает список, состоящий из 5 произвольных чисел.
# Найти количество положительных чисел среди них.

def count_positive_numbers(numbers_list):
    count = 0   # Задаем переменную, обозначающую количество положительных чисел в списке. Изначально оно равно 0.
    for num in numbers_list:
        if num > 0:
            count += 1
    return count

# Пример использования:
numbers_list = [8, -25, -9, 0, 16]
result = count_positive_numbers(numbers_list)
print(f"Количество положительных чисел: {result}")  # Выведет: 2



# 6. Функция на вход получает 2 переменные.
# a. Кол-во лет (int)
# b. Кол-во месяцев (int)
# Вывести в консоль количество дней за это время. Считать, что в каждом месяце 29 дней.


def days_quantity(years, months):
    if months < 0 or months > 12:
        print("Ошибка: количество месяцев должно быть от 0 до 11")
        return  # выходим из функции

    total_days = years * 12 * 29 + months * 29
    print(f"Общее количество дней: {total_days}")


# Пример использования:
days_quantity(2, 3)  # Общее количество дней: 783
days_quantity(1, 12)  # Общее количество дней: 696
days_quantity(0, -5)  # Ошибка: количество месяцев должно быть от 0 до 11