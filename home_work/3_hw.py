# 2. Функция на вход получает два произвольных числа.
# Вывести в консоль “yes”, если они отличаются друг от друга на 135, иначе вывести на экран “No”

def difference(num_1, num_2):
    if (num_1 - num_2) == 135:
        print('Yes')
    elif (num_2 - num_1) == 135:
        print('Yes')
    else:
        print('No')

# Проверка
difference(2, 139) #Выводит "No"
difference(0, 135) #Выводит "Yes"
difference(270, 135) #Выводит "Yes"


# 3. Функция на вход получает произвольное число от 1 до 12 (номер месяца).
# Вывести название сезона года в консоль (зима, весна, лето, осень)

def month_number(month):
    if month == 1 or month == 2 or month == 12:
        print("Зима")
    elif month in range (3, 6):
        print("Весна")
    elif month in range (6, 9):
        print("Лето")
    elif month in range (9, 12):
        print("Осень")
    else:
        print("Введите корректное число месяца (от 1 до 12)")

